package main

import "fmt"

func main() {
	fmt.Println("Comparações: ")
	fmt.Println("Igual:", "Banana" == "Banana")
	fmt.Println("Diferente:", 3 != 2)
	fmt.Println("Maior que:", 3 > 2)
	fmt.Println("Menor que:", 3 < 2)
	fmt.Println("Maior ou igual a:", 3 >= 2)
	fmt.Println("Menor ou igual a:", 3 <= 2)

}
